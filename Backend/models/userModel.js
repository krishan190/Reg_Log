import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    address: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    }
    ,
    city: {
        type: String,
        required: true
    }
},
    {
        timestamps: true
    }

);

export default mongoose.model("user", userSchema);